package com.ulsa.newsapp.Model;

public class Icons {
    private String url;
    private int width, height, bytes;
    private String format, shalsum;
    private Object error;

    public String getUrl() {
        return url;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public int getBytes() {
        return bytes;
    }

    public String getFormat() {
        return format;
    }

    public String getShalsum() {
        return shalsum;
    }

    public Object getError() {
        return error;
    }
}
